    public void insert(){
        try {
                    
//            int id = Integer.parseInt(jTextField2.getText());
            String name = jTextField1.getText();
       
            String sql ="INSERT INTO course (course_name)VALUES('"+name+"')";
            pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Succsess");
            tableload();
            clear();
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public void update(){
        
        try {
//            String Id = jTextField2.getText();
            String Id = PrevId.getText();
            if(!Id.equals("")){
                String name =  jTextField1.getText();

                String sql="UPDATE course SET course_name='"+name+"' WHERE id='"+Id+"'";
                pst=conn.prepareCall(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Updated");
                tableload();
                clear();
            }            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
     public void search(){
        String srch=jTextField3.getText(); 
    
        try {
            String sql="SELECT * FROM course WHERE id LIKE '%"+srch+"%' or course_name LIKE '%"+srch+"%'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
