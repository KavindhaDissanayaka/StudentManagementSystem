    public void search(){
        String srch=search.getText(); 
    
        try {
            String sql="SELECT * FROM admin WHERE id LIKE '%"+srch+"%' OR addname LIKE '%"+srch+"%' ";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void update (){
        String addn;
        String addt;
        String addni;
        String addm;
        String addp;
        String addad;
        String id_text = id.getText();
        
        if(!id_text.equals("")){
            try {
                addn = an.getText();
                addt =at.getText();
                addni = anic.getText();
                addm=ae.getText();
                addp=ap.getText();
                addad=aa.getText();
                String sql="UPDATE admin SET addname='"+addn+"',addtell='"+addt+"',addnic='"+addni+"',addemail='"+addm+"',addpassword='"+addp+"',addadd='"+addad+"' WHERE id='"+id_text+"'";
                pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null,"Updated");
                tableload();
                clear();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }  
        }

    }
